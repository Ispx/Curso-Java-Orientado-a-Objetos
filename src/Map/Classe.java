package Map;

public class Classe {

	private String nome;
	private String cidade;
	
	
	
	
	public Classe(String nome, String cidade) {
		this.nome = nome;
		this.cidade = cidade;
	}
	
	
	public String getNome() {
		return this.nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCidade() {
		return this.cidade;
	}
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	
	
	
}
